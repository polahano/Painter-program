    private void formMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseReleased
        myShape.setX2(evt.getX());
        myShape.setY2(evt.getY());
        repaint();
        storing_arr.add(myShape);
        //storing objects in array in order to be able to undo,redo and delete all
        myShape = myShape.get();
        // to point to a new object
    }//GEN-LAST:event_formMouseReleased

    private void formMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseDragged
        // if and else for differentiating between pen and ohter shapes 
        if (myShape instanceof Pen) {
            myShape.setX2(evt.getX());
            myShape.setY2(evt.getY());
            repaint();
            storing_arr.add(myShape);
            //storing objects in array in order to be able to undo,redo and delete all
            myShape = myShape.get();
            // to point to a new object

        } else {
            myShape.setX2(evt.getX());
            myShape.setY2(evt.getY());
            repaint();
        }
    }//GEN-LAST:event_formMouseDragged

