    @Override
    public void paint(Graphics g) {
        super.paint(g);
        try {
            if (jCheckBox1.isSelected()) {
                myShape.setFilled(true);
            }
            myShape.draw(g);
        } catch (NullPointerException e) {
            System.out.println(e);
        }
        for (Shape S2 : storing_arr) {
            g.setColor(S2.getColor());
            S2.draw(g);
        }
    }

